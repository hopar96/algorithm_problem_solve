# https://leetcode.com/problems/number-of-islands/
from collections import deque


def numIslands(grid):
    row = len(grid)
    col = len(grid[0])
    visited = [[False] * col for _ in range(row)]
    cnt = 0
    dirX = [0, 0, -1, 1]
    dirY = [-1, 1, 0, 0]

    def bfs(y, x):
        queue = deque()
        queue.append((y, x))

        while queue:
            tmpY, tmpX = queue.popleft()
            visited[tmpY][tmpX] = True
            for i in range(4):
                tarY = tmpY - dirY[i]
                tarX = tmpX - dirX[i]
                if 0 <= tarY < row and 0 <= tarX < col:
                    if grid[tarY][tarX] == "1" and not visited[tarY][tarX]:
                        visited[tarY][tarX] = True
                        queue.append((tarY, tarX))

    for r in range(row):
        for c in range(col):
            if grid[r][c] == "1" and not visited[r][c]:
                cnt += 1
                bfs(r, c)

    return cnt



